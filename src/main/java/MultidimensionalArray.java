public class MultidimensionalArray {
    public static void main(String[] args) {
            int [][] a={{1,-2,3},{-4,-5,6,9},{7}};
        for (int[] ints : a) {
            System.out.println("");
            for (int anInt : ints) {
                System.out.print(anInt);

            }

        }
    }
}
